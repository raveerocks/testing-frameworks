package io.raveerocks;

public class LongArithmetic {

    public long add(long a, long b){
        return a+b;
    }

    public long subtract(long a, long b){
        return a-b;
    }

    public long multiply(long a, long b){
        return a*b;
    }

    public long divide(long a, long b){
        return a/b;
    }
}
